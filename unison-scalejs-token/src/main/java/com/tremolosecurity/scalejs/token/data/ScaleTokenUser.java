package com.tremolosecurity.scalejs.token.data;

public class ScaleTokenUser {
	
	String displayName;
	Object token;
	
	public ScaleTokenUser() {
		
	}

	
	public String getDisplayName() {
		return displayName;
	}

	public void setDisplayName(String displayName) {
		this.displayName = displayName;
	}

	public Object getToken() {
		return token;
	}

	public void setToken(Object token) {
		this.token = token;
	}
	
	
	
}
